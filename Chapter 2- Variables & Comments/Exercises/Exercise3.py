# Define the person's name with whitespace characters
name = "\t\n  Epic Tetus  \n\t"

# Print the name with whitespace
print("Name with whitespace:", name)

# Print the name using lstrip() to remove leading whitespace
print("Name using lstrip():", name.lstrip())

# Print the name using rstrip() to remove trailing whitespace
print("Name using rstrip():", name.rstrip())

# Print the name using strip() to remove both leading and trailing whitespace
print("Name using strip():", name.strip())
